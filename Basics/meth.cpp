#include <iostream>
#include <cmath>
using namespace std;
int main(){
    //min functions
    cout<<min(6,13)<<endl;
    //max functions
    cout<<max(6,13)<<endl;
    //square root of a number
    cout << sqrt(64)<<endl;
    //log of a number
    cout << log(2)<<endl;
    //rounding a number
    cout << round(2.6)<<endl;
    //Mod of a number
    cout << abs(-2)<<endl;
    //cube root of a number
    cout<<cbrt(27)<<endl;
    //Cieling Value or the maximum value to be  picked.
    cout<<ceil(85.657)<<endl;
    //Floor value or the minimum value of that range
    cout<<floor(85.657)<<endl;
    //power of a number ie x power of y
    cout<<pow(4,2);
    return 0;
}